// Examples

// print from 1 to 10
for (i=1;i<=10;i++){
    cout << i << endl;
}

// print from 10 to 1
for (i=10;i>=1;i--){
    cout << i << endl;
}

// print odd numbers from 1 to 10
for (i=1;i<=10;i=i+2){
    cout << i << endl;
}

